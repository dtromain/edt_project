package edtproject.CodePrincipal;

/**
 * L'énumération TypeUtilisateur permet de définir un statut propre à chaque 
 * type d'utilisateur pour la connexion au gestionnaire d'emploi du temps.
 */
public enum TypeUtilisateur {

    /**
     * Statut des étudiants
     */
    ELEVE,

    /**
     * Statut des Professeurs
     */
    PROFESSEUR,

    /**
     * Statut du directeur des études
     */
    DIRECTEUR;
}
