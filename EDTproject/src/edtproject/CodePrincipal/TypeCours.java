package edtproject.CodePrincipal;

/**
 * L'énumération TypeCours permet de définir un type de cours différent pour 
 * gérer les cours matérielles dans le gestionnaire d'emploi du temps.
 */
public enum TypeCours {

    /**
     * Cours magistral
     */
    COURS,

    /**
     * Travaux dirigés
     */
    TD,

    /**
     * Travaux pratiques
     */
    TP;
}
